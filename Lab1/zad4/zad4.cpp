#include<iostream>
using namespace std;
int main()
{
	float a, b, c, d;
	cout << "Podaj pierwsza liczbe = "; cin >> a;
	cout << "Podaj droga liczbe = "; cin >> b;
	cout << "Podaj trzecia liczbe = "; cin >> c;
	cout << "Podaj czwarta liczbe = "; cin >> d;
	cout << "Srednia arytmetyczna a,b,c,d wynosi = " << (a + b + c + d) / 4;
	return 0;
}